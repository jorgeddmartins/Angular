
// Runs Animation For 2 Elements
function panelAnim2() {

    // Adding Animation Elemet
    //mSettings.bopanel
    $('.bodyPanel')

        // 2nd Position For 2 Elements
        .delay(2000).animate({ top: '100%' }, 650)

        // 1st Position For 2 Elements
        .delay(650).animate({ top: 0 }, 650, panelAnim2); 
}


